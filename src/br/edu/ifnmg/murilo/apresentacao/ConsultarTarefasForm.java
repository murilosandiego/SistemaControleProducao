    private void editarTarefa() {
        int linhaSelecionada = tblResultado.getSelectedRow();
        if(linhaSelecionada != -1){
            Tarefa tarefaSelecionada = tarefas.get(linhaSelecionada);
            
            if(this.editarTarefaForm != null){
                this.editarTarefaForm.dispose();
            }
            this.editarTarefaForm= 
                    new CadastroTarefaForm(this, tarefaSelecionada);
            this.editarTarefaForm.setVisible(true);
            
            
        }else{
            String mensagem = "Nenhuma tarefa selecionado.";
                JOptionPane.showMessageDialog(this,
                        mensagem,
                        "Edição de tarefa",
                        JOptionPane.INFORMATION_MESSAGE);
        }
    }

    private void exibirTelaCadastroTarefa() {

        if(this.novaTarefaForm == null){
            this.novaTarefaForm = new CadastroTarefaForm(this);
        }
        novaTarefaForm.setVisible(true);
        novaTarefaForm.toFront();
        
    }

    private void excluirTarefa(){
        try {
            int linhaSelecionada = tblResultado.getSelectedRow();
            if (linhaSelecionada != -1) {
                Tarefa tarefaSelecionada = tarefas.get(linhaSelecionada);

                int resposta;
                String mensagem = "Deseja excluir a Tarefa ( "
                        + tarefaSelecionada.getNome() + " )?";
                String titulo = "Exclusão de Tarefa";
                resposta = JOptionPane.showConfirmDialog(this,
                        mensagem, titulo, JOptionPane.YES_NO_OPTION);

                if (resposta == JOptionPane.YES_OPTION) {
                    
                    TarefaBO tarefaBO = new TarefaBO();
                    tarefaBO.excluir(tarefaSelecionada.getId());
                    
                    String mensagemSuceso = "Tarefa "
                            + tarefaSelecionada.getNome()
                            + "excluída com sucesso.";
                    JOptionPane.showMessageDialog(this,
                            mensagemSuceso, titulo,
                            JOptionPane.INFORMATION_MESSAGE);

                    this.carregarTabelaTarefas();
                }
            } else {
                String mensagem = "Nenhuma tarefa selecionado.";
                JOptionPane.showMessageDialog(this,
                        mensagem,
                        "Exclusão de tarefa",
                        JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            String mensagem = "Erro inesperado! Informe a mensagem de erro ao administrador do sistema.";
            mensagem += "\nMensagem de erro:\n" + e.getMessage();
            JOptionPane.showMessageDialog(this, mensagem, "Exclusão de fornecedor", JOptionPane.ERROR_MESSAGE);
            this.dispose();
        }

    }

