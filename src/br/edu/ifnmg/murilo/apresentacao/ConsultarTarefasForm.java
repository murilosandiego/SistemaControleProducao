    private class ModeloTabelaTarefas extends AbstractTableModel {

        @Override
        public String getColumnName(int coluna) {
            if (coluna == 0) {
                return "Tarefa";
            } else if (coluna == 1) {
                return "Descrição";
            }else if (coluna == 2) {
                return "Valor Diária";
            } else {
                return "Observação";
            }
        }

        @Override
        public int getRowCount() {
            return tarefas.size();
        }


        @Override
        public int getColumnCount() {
            return 4;
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            Tarefa tarefa = tarefas.get(rowIndex);
            if (columnIndex == 0) {
                return tarefa.getNome();
            } else if (columnIndex == 1) {
                return tarefa.getDescricao();
            }else if (columnIndex == 2) {
                return tarefa.getValorDiaria();
            } else {
                return tarefa.getObservacao();
            }
        }

    }

