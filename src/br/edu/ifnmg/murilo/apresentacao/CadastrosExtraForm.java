    private void salvarGastosExtras() {

        try {
            this.recuperaCamposTela();

            if (editaGastoExtra != null) {
                this.chamarBOParaEditar();
            } else {

                GastosExtraBO gastosExtraBO = new GastosExtraBO();
                gastosExtraBO.inserir(this.gastosExtra);
            }

            JOptionPane.showMessageDialog(this, "Gastos extras cadastrado com sucesso!", "Cadastro de Tijolo", JOptionPane.INFORMATION_MESSAGE);
            this.limpaCamposTela();

        } catch (SistemaControleProducaoException e) {
            String mensagem = "Erro ao realizar opera√ß√£o:\n" + e.getMessage();
            JOptionPane.showMessageDialog(this, mensagem, "Cadastro de Gastos Extras", JOptionPane.ERROR_MESSAGE);
        }catch (Exception e) {
            String mensagem = "Erro inesperado! Informe a mensagem de erro ao administrador do sistema.";
            mensagem += "\nMensagem de erro:\n" + e.getMessage();
            JOptionPane.showMessageDialog(this, mensagem, "Cadastro de Gastos extras", JOptionPane.ERROR_MESSAGE);
            this.dispose();

        }
    }
    
    private void chamarBOParaEditar() throws SQLException{
        GastosExtraBO gastosExtraBO = new GastosExtraBO();
                gastosExtra.setId(editaGastoExtra.getId());
                gastosExtraBO.atualizar(gastosExtra);
    }

    private void editarGastosExtras() {
        try {
            this.buscaGastosExtra();
            this.inicializaCamposTela();
        } catch (Exception e) {
            String mensagem = "Erro inesperado! Informe a mensagem de erro ao administrador do sistema.";
            mensagem += "\nMensagem de erro:\n" + e.getMessage();
            JOptionPane.showMessageDialog(this, mensagem, "Cadastro de Gastos extras", JOptionPane.ERROR_MESSAGE);
            this.dispose();

        }
    }

    private void buscaGastosExtra() throws SQLException {
        GastosExtraBO gastosExtraBO = new GastosExtraBO();
        this.editaGastoExtra = gastosExtraBO.buscar();

    }

    private void inicializaCamposTela() {
        if (!(this.editaGastoExtra.getOutrasGastos() == 0 || this.editaGastoExtra.getOutrasGastos() == 0.0)) {
            txtOutrosGastos.setText(String.valueOf(editaGastoExtra.getOutrasGastos()));
        }
        txtTarifaAgua.setText(String.valueOf(editaGastoExtra.getTarifaAgua()));
        txtTarifaEletricidade.setText(String.valueOf(editaGastoExtra.getTarifaLuz()));
    }

    private void limpaCamposTela() {
        txtOutrosGastos.setText("");
        txtTarifaAgua.setText("");
        txtTarifaEletricidade.setText("");
    }
