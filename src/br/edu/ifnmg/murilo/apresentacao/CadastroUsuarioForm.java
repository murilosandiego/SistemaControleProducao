    private void salvarUsuario() {
        try {
            this.recuperaCamposTela();
            UsuarioBO usuarioBO = new UsuarioBO();
            usuarioBO.verificaUsuarioLogin(usuarioEmEdicao);
            usuarioBO.inserir(usuarioEmEdicao);
            this.dispose();
            exibirMensagemSucesso("Usuario Cadastrado com sucesso!", "Cadastro de Usuário");
            if (consultarUsuarioForm != null) {
                this.consultarUsuarioForm.carregarTabelaUsuarios();
            }
        } catch (SistemaControleProducaoException e) {
            exibirMensagemErro(e.getMessage(), "Cadastrao de Usuario");
        } catch (Exception e) {
            String mensagem = "Erro inesperado! Informe a mensagem de erro ao administrador do sistema.";
            mensagem += "\nMensagem de erro:\n" + e.getMessage();
            JOptionPane.showMessageDialog(this, mensagem, "Cadastro de fonecedor", JOptionPane.ERROR_MESSAGE);
            this.dispose();
        }
    }

    private void editarUsuario() {
        try {
            this.recuperaCamposTela();
            UsuarioBO usuarioBO = new UsuarioBO();

            usuarioBO.atualizar(usuarioEmEdicao);

            JOptionPane.showMessageDialog(this, "Usuário editado com sucesso!", "Cadastro de Usuário", JOptionPane.INFORMATION_MESSAGE);
            this.consultarUsuarioForm.carregarTabelaUsuarios();
            dispose();
        } catch (SistemaControleProducaoException e) {
            String mensagem = "Erro ao realizar operação:\n" + e.getMessage();
            JOptionPane.showMessageDialog(this, mensagem, "Cadastro de Usuário", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            String mensagem = "Erro inesperado! Informe a mensagem de erro ao administrador do sistema.";
            mensagem += "\nMensagem de erro:\n" + e.getMessage();
            JOptionPane.showMessageDialog(this, mensagem, "Cadastro de Usuário", JOptionPane.ERROR_MESSAGE);
            this.dispose();
        }
    }

    private void exibirMensagemErro(String mensagem, String titulo) {
        JOptionPane.showMessageDialog(this, mensagem, titulo, JOptionPane.ERROR_MESSAGE);
    }

    private void exibirMensagemSucesso(String mensagem, String titulo) {
        JOptionPane.showMessageDialog(this, mensagem, titulo, JOptionPane.INFORMATION_MESSAGE);
    }
