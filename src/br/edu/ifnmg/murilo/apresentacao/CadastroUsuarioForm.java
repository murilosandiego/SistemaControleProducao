    private void recuperaCamposTela() {
        String nome = txtNome.getText();
        String login = txtLogin.getText().trim();
        String senha = txtSenha.getText();
        String senha2 = txtRepitaSenha.getText();

        String mensagem = "";

        if (login.isEmpty()) {
            mensagem = "Campo LOGIN é obrigatório.";
        }

        if (senha.isEmpty()) {
            mensagem = mensagem + "\nCampo SENHA é obrigatório.";
        }

        if (senha2.isEmpty()) {
            mensagem = mensagem + "\nCampo REPITA A SENHA é obrigatório.";
        }

        if (mensagem == "" || mensagem == null) {
            if (senha.equals(senha2)) {
                try {
                    this.usuarioEmEdicao.setNome(nome);
                    this.usuarioEmEdicao.setLogin(login);
                    this.usuarioEmEdicao.setSenha(Criptografica.exemploMD5(senha));
                } catch (NoSuchAlgorithmException ex) {
                    Logger.getLogger(CadastroUsuarioForm.class.getName()).log(Level.SEVERE, null, ex);
                } catch (UnsupportedEncodingException ex) {
                    Logger.getLogger(CadastroUsuarioForm.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                throw new SenhaInvalidaException("As senhas são diferentes. \n Elas devem ser iguais");
            }
        } else {
            throw new CampoVazioException(mensagem);
        }

    }

    private void inicializaCamposTela() {
        txtLogin.setText(usuarioEmEdicao.getLogin());
        txtNome.setText(usuarioEmEdicao.getNome());
    }

